package Entidades;

public class Operaciones {

	public int a=2, b=2;                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                ;


public int Sumar(){
	int sum;
sum= a+b;
System.out.println("El resultado de la suma es :" +sum);
	return sum;
}

public int Resta(){
int resta;
	resta= a-b;
	System.out.println("El resultado de la suma es :" +resta);
	return resta;
}

public int Multiplicacion(){
int mult;
	mult= a*b;
	System.out.println("El resultado de la suma es :" +mult);
	return mult;
}


}